import { NextIntlClientProvider, hasLocale } from 'next-intl';
import { notFound } from 'next/navigation';
import { routing } from '@/i18n/routing';
import "./globals.css";
import Header from '@/component/Header';
import { UserProvider } from '@/context/userContext';
import { CategoryProvider } from '@/context/categoryContext';
import { SubCategoryProvider } from '@/context/subCategoryContext';
import { NewPlaceProvider } from '@/context/newPlaceContext';
import { TourProvider } from '@/context/tourContext';
import Footer from '@/component/common/Footer';
import { ActiveMenuProvider } from '@/context/activeMenuContext';
import { ToastContainer } from 'react-toastify';
import { CountryProvider } from '@/context/countryContext';
import { CartProvider } from '@/context/cartContext';
import { BookingProvider } from '@/context/bookingContext';
import { DestinationWiseTourProvider } from '@/context/destinationWiseTourContext';
import Head from 'next/head';


export const metadata = {
  title: "Ip Tours",
  description: "Generated by create next app",
};


export default async function LocaleLayout({
  children,
  params
}) {
  // Ensure that the incoming `locale` is valid
  const { locale } = await params;
  if (!hasLocale(routing.locales, locale)) {
    notFound();
  }

  return (
    <html lang={locale}>
     <Head>
        <link
          rel="stylesheet"
          href="https://static.micuentaweb.pe/static/js/krypton-client/V4.0/ext/neon-reset.min.css"
        />
        <script
        defer
          src="https://api.micuentaweb.pe/static/js/krypton-client/V4.0/stable/kr-payment-form.min.js"
          async
        ></script>
      </Head>
      <body>
        <ActiveMenuProvider>
          <NextIntlClientProvider>
            <UserProvider>
              <CartProvider>
                <DestinationWiseTourProvider>
                  <CountryProvider>
                    <TourProvider>
                      <NewPlaceProvider>
                        <CategoryProvider>
                          <SubCategoryProvider>
                            <BookingProvider>
                              <Header />
                              {children}
                              <Footer />
                              <ToastContainer />
                            </BookingProvider>
                          </SubCategoryProvider>
                        </CategoryProvider>
                      </NewPlaceProvider>
                    </TourProvider>
                  </CountryProvider>
                </DestinationWiseTourProvider>
              </CartProvider>
            </UserProvider>
          </NextIntlClientProvider>
        </ActiveMenuProvider>
      </body>
    </html>
  );
}

